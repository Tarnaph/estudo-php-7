<h6><b>Exercício 02: </b></h6>
<p class="justify">
Assumindo o exemplo fictício de uma locadora, 
modele e construa os códigos necessários para 
as seguintes classes de um sistema: </p> 

<p><b>Classe Usuario:</b> deve conter o <b>nome</b>, <b>telefone</b> 
e <b>endereço </b>do usuário da locadora, e também o 
<b>número de filmes</b> que ele já locou. Deve conter 
um método que <b>exiba estas informações.</b> </p>

<p><b>Classe Funcionario:</b> deve conter o <b>nome, telefone 
e endereço</b> do funcionário da locadora, e o valor 
do seu <b>salário</b>. Deve conter um método que <b>exiba estas informações.</b></p>

<p><b>Classe CD:</b> deve conter <b>o nome do artista</b>,<b> o nome do álbum</b> e <b>estilo</b>.
Deve conter um método que <b>exiba estas informações. </b></p>

<p><b>Classe DVD: </b>deve conter o <b>título do DVD e o estilo. </b>
Deve conter um método que exiba estas <b>informações</b>. </p>

<p><b>Classe Bluray:</b> deve conter o título do Bluray, estilo e 
resolução (se é HD ou FullHD). Deve conter um método 
que exiba estas informações. </p>

<p><b>Classe Locacao:</b> deve conter o objeto do Usuario e do 
produto que foi locado. Deve conter um método que 
exiba estas informações. </p>

<p><b>Classe Locadora:</b> todos os objetos do sistema devem 
ser armazenados dentro de um <b>único objeto Locadora</b>, 
que forneça os <b>métodos apropriados</b> para <b>criação</b> de todos os 
objetos citados, e também métodos de <b>exibição</b> dos mesmos. 
Por exemplo: <b>método criarUsuario, criarFuncionario, criarDVD, 
listaUsuarios, listaFuncionarios, listaProdutos</b> etc... 
Modele e crie quantas classes extras achar necessário, de 
modo a evitar redundância de código e manter o seu código organizado. </p>

<p>Utilize as recomendações apresentadas em aula, como por exemplo, 
modificadores de acessos private, protecte e public quando necessários, 
bem como o uso de métodos getters e setters.</p>
